CREATE TABLE NGUOIDUNG (
	SDT VARCHAR(10) PRIMARY KEY,
  	EMAIL VARCHAR(50),
 	HOTEN VARCHAR(50),
 	NAMSINH DATE,
 	MATKHAU VARCHAR(50),
	GIOITINH CHAR,
);

CREATE TABLE KHACHHANG(
	SDT VARCHAR(10) PRIMARY KEY,
	FOREIGN KEY(SDT) REFERENCES NGUOIDUNG(SDT),
	XA VARCHAR(50),
	HUYEN VARCHAR(50),
	TINH VARCHAR(50),
	CHITIET VARCHAR(50)
);

CREATE TABLE NHANVIEN (
	SDT VARCHAR(10) PRIMARY KEY,
  	FOREIGN KEY(SDT) REFERENCES NGUOIDUNG(SDT),
  	LUONG FLOAT,
  	NGAYBATDAU DATE,
);

CREATE TABLE TRAM (
	NGUOIQUANLY VARCHAR(10),
	FOREIGN KEY (NGUOIQUANLY) REFERENCES NGUOIQUANLY(SDT),
	STT INT PRIMARY KEY,
	XA VARCHAR(50),
	HUYEN VARCHAR(50),
	TINH VARCHAR(50),
	CHITIET VARCHAR(50)
);

CREATE TABLE NGUOIQUANLY (
	SDT VARCHAR(10) PRIMARY KEY,
	FOREIGN KEY(SDT) REFERENCES NHANVIEN(SDT)
);


CREATE TABLE TRAMDANGLAMVIEC (
	NHANVIEN VARCHAR(10) PRIMARY KEY,
	FOREIGN KEY(NHANVIEN) REFERENCES NHANVIEN(SDT),
	IDTRAM INT,
	FOREIGN KEY(IDTRAM) REFERENCES TRAM(STT),
);

CREATE TABLE CAPBAC(
  NGUOIQUANLY VARCHAR(10) FOREIGN KEY REFERENCES NGUOIQUANLY(SDT),
  CAPBAC VARCHAR(50),
);

CREATE TABLE DONHANG(
	NGUOITAODON VARCHAR(10),
	FOREIGN KEY(NGUOITAODON) REFERENCES KHACHHANG(SDT),
	GIA FLOAT,
	SDTNGUOINHAN VARCHAR(10),
	HOTENNGUOINHAN VARCHAR(50),
	TINHTRANG VARCHAR(20),
	QUANGDUONG FLOAT,
	NGAYTAO DATE,
	MADONHANG CHAR(10) PRIMARY KEY,
	XA VARCHAR(50),
	HUYEN VARCHAR(50),
	TINH VARCHAR(50),
	CHITIET VARCHAR(50)
)

CREATE TABLE TUYEN(
	TINHTRANG VARCHAR(20),
	STT INT NOT NULL,
	MADONHANG CHAR(10) NOT NULL,
	FOREIGN KEY(MADONHANG) REFERENCES DONHANG(MADONHANG),
	PRIMARY KEY (STT, MADONHANG),
	XA_BD VARCHAR(50),
	HUYEN_BD VARCHAR(50),
	TINH_BD VARCHAR(50),
	CHITIET_BD VARCHAR(50),
	XA_KT VARCHAR(50),
	HUYEN_KT VARCHAR(50),
	TINH_KT VARCHAR(50),
	CHITIET_KT VARCHAR(50)
)




















